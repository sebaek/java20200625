package chap02;

public class HelloWorld {
	
	public static void main(String[] args) {
		int i;
		i = 40;
		
		int j = 3;
		
		int z = i + j;
		System.out.println(z);
		
		if (z > 3) {
			System.out.println("z 는 3보다 크다.");
		} else {
			System.out.println("z 는 3보다 크지 않다.");
		}
		
		for (int k = 0; k < 5; k++) {
			System.out.println(k);
		}
	}

}










